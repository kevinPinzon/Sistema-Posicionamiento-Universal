    public void greyScales() {
        for (int i = 0; i < inicial_Image.getWidth(); i++) {
            for (int j = 0; j < inicial_Image.getHeight(); j++) {
                colorTemp = new Color(this.inicial_Image.getRGB(i, j));
                mediaPixel = (int) ((colorTemp.getRed() + colorTemp.getGreen() + colorTemp.getBlue()) / 3);
                colores = (mediaPixel << 16) | (mediaPixel << 8) | mediaPixel;
                inicial_Image.setRGB(i, j, colores);
            }
        }
        Image dimg = inicial_Image.getScaledInstance(lbImagen.getWidth(), lbImagen.getHeight(),
                Image.SCALE_SMOOTH);
        lbImagen.setIcon(new ImageIcon(dimg));
    }

    public void preorder(Nodo root) {
        if (root != null) {
            dibujar(root, ((BufferedImage) root.getValue()).getWidth(), ((BufferedImage) root.getValue()).getHeight());
            preorder(root.getNE());
            preorder(root.getNW());
            preorder(root.getSE());
            preorder(root.getSW());
        }
    }

    public void dibujar(Nodo nodo, int width, int height) {
        Color blackColor = new Color(178, 34, 34);
        //linea vertical
        for (int i = 0; i < ((BufferedImage) nodo.getValue()).getHeight(); i++) {
            ((BufferedImage) nodo.getValue()).setRGB(i, i, -1);
            ((BufferedImage) nodo.getValue()).setRGB(width / 2, i, blackColor.getRGB());

        }
        //linea horizontal
        for (int j = 0; j < ((BufferedImage) nodo.getValue()).getWidth(); j++) {
            ((BufferedImage) nodo.getValue()).setRGB(j, height / 2, blackColor.getRGB());
        }

    }

