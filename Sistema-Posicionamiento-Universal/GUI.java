    private void btn_BuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_BuscarMouseClicked
        if (seleccionado.showDialog(null, "ABRIR ") == JFileChooser.APPROVE_OPTION) {
            archivo = seleccionado.getSelectedFile();
            if (archivo.getName().endsWith("jpg") || archivo.getName().endsWith("png") || archivo.getName().endsWith("gif")) {
                try {
                    inicial_Image = ImageIO.read(archivo);
                } catch (IOException e) {
                    //e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "error al abrir la imagen");
                }
                greyScales();
            } else {
                JOptionPane.showMessageDialog(null, "por favor seleccione una imagen");
            }
            lb_path_file.setText(archivo.getPath());
        }
    }//GEN-LAST:event_btn_BuscarMouseClicked

    private void btn_procesarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_procesarMouseClicked
        if (archivo == null) {
            JOptionPane.showMessageDialog(this, "No se ha buscado la imagen", "Info", JOptionPane.INFORMATION_MESSAGE);
        } else {
            int profundidad;
            profundidad = Integer.parseInt(JOptionPane.showInputDialog(this, "Debe ingresar la profundidad\nSi no la ingresa no podra ver nada",
                    "Advertencia", JOptionPane.WARNING_MESSAGE));
            quad = new QuadTree(inicial_Image, profundidad);
        }
    }//GEN-LAST:event_btn_procesarMouseClicked

    private void btn_resultsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_resultsMouseClicked
        try {
            if (quad == null) {
                JOptionPane.showMessageDialog(this, "No puede ejecutarse esta accion");
            } else {
                preorder(quad.getRaiz());
                Image dimg = ((BufferedImage) quad.getRaiz().getValue()).getScaledInstance(317, 261, Image.SCALE_SMOOTH);
                lb_result.setIcon(new ImageIcon(dimg));
                this.sub_result.setLocationRelativeTo(null);
                this.sub_result.setModal(true);
                this.sub_result.pack();
                this.sub_result.setVisible(true);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error inesperado", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_resultsMouseClicked

